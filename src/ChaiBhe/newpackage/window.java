    private void bm2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bm2ActionPerformed
        if(tb.isRowSelected(selected)){
            double moneySelected=Double.valueOf(tb.getValueAt(selected,3).toString().replace(",",""));
            System.out.println("แสดงจำนวนเงิน"+moneySelected);
            total-=moneySelected;
            lbtotal.setText("ราคารวม : "+df.format(total)+"  บาท  ");
            if(total==0){
                lbtotal.setText("ราคารวม : 0.00 บาท  ");
            }
            model.removeRow(selected);
        }else{
            JOptionPane.showMessageDialog(this,"กรุณาเลือกแถวในตาราง","คำเตือน",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bm2ActionPerformed

    private void tbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMouseClicked
        selected=tb.getSelectedRow();
        String name =tb.getValueAt(selected,0).toString();
        System.out.println("Name = "+name);
        System.out.println("แถวที่เลือก="+selected);
        t2.setText(tb.getValueAt(selected,2).toString());
        for(int i = 0; i < cbMenu.getItemCount(); i++){
            if(name.equals(cbMenu.getItemAt(i))){
                cbMenu.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_tbMouseClicked

    private void bm3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bm3ActionPerformed
        double before=Double.valueOf(tb.getValueAt(selected,1).toString())*Double.valueOf(tb.getValueAt(selected,2).toString());
        System.out.println("before = "+before);
        tb.setValueAt(t2.getText(),selected,2);
        int amount=Integer.valueOf(t2.getText());
        double bnew=amount*Double.valueOf(tb.getValueAt(selected,1).toString());
        tb.setValueAt(df.format(bnew),selected,3);
        total = total-before+bnew;
        lbtotal.setText("ราคารวม : "+df.format(total)+"  บาท  ");
    }//GEN-LAST:event_bm3ActionPerformed
    public boolean checkMenu(){
        for (int i = 0; i < tb.getRowCount(); i++){
            if(cbMenu.getSelectedItem().toString().equals(tb.getValueAt(i,0))){
                n=i;
                System.out.println("รายการอาหารซ้ำกัน"+"ที่แถว = "+n);
                return true;  
            }  
        }
        return false;
    }
