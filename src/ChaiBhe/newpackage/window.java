    private void bm1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bm1ActionPerformed
        Vector v=new Vector();
        if(t2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"กรุณาป้อนจำนวนอาหาร","คำเตือน",JOptionPane.ERROR_MESSAGE);
        }if(checkMenu()){
            int a=Integer.parseInt(model.getValueAt(n,2).toString())+Integer.parseInt(t2.getText());
            System.out.println("a="+a);
            model.setValueAt(a,n,2);
            double price=a*Double.valueOf(tb.getValueAt(n,1).toString());
            System.out.print("ราคาใหม่="+price);
            model.setValueAt(df.format(price),n,3);
            money=Double.valueOf(t1.getText())*Double.valueOf(t2.getText());
            total+=money;
            lbtotal.setText("ราคารวม : "+df.format(total)+"บาท");
        }else{
            v.add(cbMenu.getSelectedItem().toString());
            v.add(t1.getText());
            v.add(t2.getText());
            money=Double.valueOf(t1.getText())*Double.valueOf(t2.getText());
            v.add(df.format(money));
            total+=money;
            model.addRow(v);
            lbtotal.setText("ราคารวม : "+df.format(total)+"  บาท  ");
        }     
    }//GEN-LAST:event_bm1ActionPerformed

